package Utils;

public class myDouble {

	public static double parseDouble(String s)
	{
		double val; 
		
		try{
			val = Double.parseDouble(s);			
		}catch(NumberFormatException e){
			val = Double.parseDouble(s.replace(",", "."));
		}
		
		return val;
	}
}
